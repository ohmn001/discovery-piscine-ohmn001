#!/usr/bin/env python3

first_input = int(input('Give me the first number: '))
secound_input = int(input('Give me the secound number: '))
print('Thank you')
Ans_plus = first_input + secound_input  
Ans_minus = first_input - secound_input  
Ans_divid = first_input / secound_input  
Ans_multi = first_input * secound_input 

print(f'{first_input} + {secound_input}  = {Ans_plus }')
print(f'{first_input} - {secound_input}  = {Ans_minus} ')
print(f'{first_input} / {secound_input}  = {Ans_divid} ')
print(f'{first_input} * {secound_input} = {Ans_multi} ')